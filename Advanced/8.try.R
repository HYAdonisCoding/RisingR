######### 牛刀小试：将两个数据源绘制在同一个坐标系中 ############
# 任务：生成两列随机数 x1 和 x2，样本量均为1000。
# x1 服从均值为0、标准差为1的正态分布；
# x2 服从均值为2、标准差为1的正态分布。
# 用 ggplot2 绘制：
#   - x1 的直方图（频率分布）
#   - x2 的核密度曲线
# 要求：它们绘制在同一个坐标系中，便于比较。

# 加载绘图包
library(ggplot2)

# 保持随机结果可重复
set.seed(1234)

# 生成两列随机数
x1 <- rnorm(1000, 0, 1)  # 标准正态分布
x2 <- rnorm(1000, 2, 1)  # 均值为2的正态分布

# 使用 ggplot2 作图
# 注意：这里不直接指定 data，而是在每个 geom 图层里单独传递数据
ggplot() +
  # 绘制 x1 的直方图，转换为密度形式（y=..density..）
  geom_histogram(aes(x = x1, y = ..density..),
                 bins = 30,               # 分箱数
                 color = "grey",          # 边框颜色
                 fill = "yellow") +       # 填充颜色
  # 绘制 x2 的核密度曲线
  geom_line(aes(x = x2),
            stat = "density",             # 使用密度估计
            color = "red",                # 曲线颜色
            size = 1) +                   # 线条粗细
  # 添加标题和坐标轴说明
  labs(title = "x1直方图（黄色）与x2核密度曲线（红色）",
       x = "取值",
       y = "密度") +
  theme_minimal()  # 使用简洁主题